/** Helper to group violations by criterion and count pages */
interface TopIssue {
  severity: string;
  wcagCriterion: string;
  description: string;
  pageCount: number;
}

function computeTopIssues(data: ReportTemplateData): TopIssue[] {
  const grouped = new Map<string, { severity: string; description: string; pages: Set<string> }>();

  for (const v of data.violations) {
    const key = `${v.wcagCriterion}:${v.severity}`;
    if (!grouped.has(key)) {
      grouped.set(key, {
        severity: v.severity,
        description: v.description,
        pages: new Set(),
      });
    }
    grouped.get(key)!.pages.add(v.pageUrl);
  }

  const issues: TopIssue[] = [];
  for (const [key, value] of grouped) {
    const [criterion] = key.split(':');
    issues.push({
      severity: value.severity,
      wcagCriterion: criterion ?? 'Unknown',
      description:
        value.description.length > 100
          ? value.description.substring(0, 100) + '...'
          : value.description,
      pageCount: value.pages.size,
    });
  }

  const severityOrder = { critical: 0, serious: 1, moderate: 2, minor: 3 };
  issues.sort((a, b) => {
    const aSev = severityOrder[a.severity as keyof typeof severityOrder] ?? 4;
    const bSev = severityOrder[b.severity as keyof typeof severityOrder] ?? 4;
    if (aSev !== bSev) return aSev - bSev;
    return b.pageCount - a.pageCount;
  });

  return issues.slice(0, 10);
}

/** Compute quick wins vs complex fixes */
function categorizeRemediations(data: ReportTemplateData): {
  quickWins: Array<{ wcagCriterion: string; description: string }>;
  complexFixes: Array<{ wcagCriterion: string; description: string }>;
} {
  const seen = new Set<string>();
  const quickWins: Array<{ wcagCriterion: string; description: string }> = [];
  const complexFixes: Array<{ wcagCriterion: string; description: string }> = [];

  const simpleRules = [
    'image-alt',
    'button-name',
    'link-name',
    'label',
    'document-title',
    'html-has-lang',
    'html-lang-valid',
    'meta-viewport',
    'duplicate-id',
  ];

  for (const v of data.violations) {
    if (seen.has(v.wcagCriterion)) continue;
    seen.add(v.wcagCriterion);

    const item = {
      wcagCriterion: v.wcagCriterion,
      description:
        v.description.length > 80 ? v.description.substring(0, 80) + '...' : v.description,
    };

    const isSimple = simpleRules.some((rule) =>
      v.description.toLowerCase().includes(rule.replace('-', ' ')),
    );

    if (isSimple || v.severity === 'minor') {
      quickWins.push(item);
    } else {
      complexFixes.push(item);
    }
  }

  return {
    quickWins: quickWins.slice(0, 5),
    complexFixes: complexFixes.slice(0, 5),
  };
}

/** Compute trend bars for SVG chart */
function computeTrendBars(data: ReportTemplateData): Array<{
  x: number;
  y: number;
  width: number;
  height: number;
  labelX: number;
  scoreY: number;
  date: string;
  score: number;
}> {
  const allScans = [
    { date: data.scan.completedAt, score: data.scan.score },
    ...data.previousScans,
  ].slice(0, 5);

  if (allScans.length === 0) return [];

  const barWidth = 50;
  const gap = 30;
  const maxHeight = 80;
  const startX = 40;

  return allScans.map((s, i) => {
    const height = (s.score / 100) * maxHeight;
    const x = startX + i * (barWidth + gap);
    const y = 90 - height;

    return {
      x,
      y,
      width: barWidth,
      height,
      labelX: x + barWidth / 2,
      scoreY: y - 5,
      date: s.date.split('/').slice(0, 2).join('/'),
      score: s.score,
    };
  });
}

/**
 * Render the Executive Summary report template.
 *
 * @param data - Complete report template data
 * @returns Rendered HTML string
 */
export function renderExecutiveTemplate(data: ReportTemplateData): string {
  const totalViolations =
    data.scan.criticalCount +
    data.scan.seriousCount +
    data.scan.moderateCount +
    data.scan.minorCount;

  const wcagCoverage = Math.max(
    0,
    Math.round(100 - (totalViolations / Math.max(data.scan.pagesScanned * 5, 1)) * 100),
  );
  const is17802Coverage = wcagCoverage;
  const gigwCoverage = Math.max(0, wcagCoverage - 5);

  const topIssues = computeTopIssues(data);
  const { quickWins, complexFixes } = categorizeRemediations(data);
  const trendBars = computeTrendBars(data);
  const certificationEligible = data.scan.score >= 80 && data.scan.criticalCount === 0;

  const templateData = {
    ...data,
    totalViolations,
    wcagCoverage,
    is17802Coverage,
    gigwCoverage,
    topIssues,
    quickWins,
    complexFixes,
    trendBars,
    certificationEligible,
    reportId: `RPT-${Date.now().toString(36).toUpperCase()}`,
  };

  const template = Handlebars.compile(EXECUTIVE_TEMPLATE);
  return template(templateData);
}
