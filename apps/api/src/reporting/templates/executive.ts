/** Handlebars helper for severity CSS class */
Handlebars.registerHelper('severityClass', function (severity: string) {
  return `severity-${severity}`;
});

/** Handlebars helper for delta display */
Handlebars.registerHelper('deltaDisplay', function (delta: number | null) {
  if (delta === null) return '—';
  if (delta > 0) return `↑ ${delta}`;
  if (delta < 0) return `↓ ${Math.abs(delta)}`;
  return '→ 0';
});

/** Handlebars helper for delta class */
Handlebars.registerHelper('deltaClass', function (delta: number | null) {
  if (delta === null) return 'score-delta-unchanged';
  if (delta > 0) return 'score-delta-improved';
  if (delta < 0) return 'score-delta-declined';
  return 'score-delta-unchanged';
});

/** Handlebars helper for compliance status */
Handlebars.registerHelper('complianceStatus', function (critical: number, serious: number) {
  if (critical === 0 && serious === 0) return 'Compliant';
  if (critical === 0) return 'Substantially Compliant';
  if (critical <= 5) return 'Partially Compliant';
  return 'Non-Compliant';
});

/** Handlebars helper for compliance class */
Handlebars.registerHelper('complianceClass', function (critical: number, serious: number) {
  if (critical === 0 && serious === 0) return 'compliance-compliant';
  if (critical === 0) return 'compliance-substantially';
  if (critical <= 5) return 'compliance-partially';
  return 'compliance-non-compliant';
});

